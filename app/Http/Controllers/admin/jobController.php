        if ($search != ""){

            $jobs = job::where('fault','LIKE',"%$search%")->orwhere('vehicle_name','LIKE',"%$search%")->get();


        }else{
            $jobs = job::all();
        }

        $data = compact('jobs','search');
        return view('admin.jobs.manage_jobs')->with($data);;

