     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request);
        $request->validate([
            'name'=>'required',
            'position'=>'required',
            'feedback'=>'required',
        ]);
        feedback::create([
            'customer_name' => $request->input('name'),
            'customer_position' => $request->input('position'),
            'feedback' => $request->input('feedback'),
            'customer_id' => auth('customer')->id()
        ]);
        return redirect()->route('customer.dashboard')->with('success', 'Thanks for your feedback !!!');
    }

